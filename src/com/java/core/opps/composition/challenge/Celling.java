package com.java.core.opps.composition.challenge;

public class Celling {
	private int height;
	private String colour;
	public Celling(int height, String colour) {
		
		this.height = height;
		this.colour = colour;
	}
	public int getHeight() {
		return height;
	}
	public String getColour() {
		return colour;
	}
	
	

}
